        // Função para formatar o número de telefone
        function formatarTelefone(input) {
            // Remove caracteres não numéricos
            var num = input.value.replace(/\D/g, '');
            
            // Adiciona a máscara "(99) 99999-9999" ao número
            var formattedNum;

            if (num.length === 11) {
                formattedNum = '(' + num.substring(0, 2) + ') ' + num.substring(2, 7) + '-' + num.substring(7, 11);
            } else {
                formattedNum = '(' + num.substring(0, 2) + ') ' + num.substring(2, 6) + '-' + num.substring(6, 10);
            }
            
            // Atualiza o valor do input
            input.value = formattedNum;
